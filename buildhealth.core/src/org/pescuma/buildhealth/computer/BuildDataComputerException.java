package org.pescuma.buildhealth.computer;

public class BuildDataComputerException extends RuntimeException {
	
	private static final long serialVersionUID = 9033333998680653832L;
	
	public BuildDataComputerException() {
		super();
	}
	
	public BuildDataComputerException(String message, Throwable cause) {
		super(message, cause);
	}
	
	public BuildDataComputerException(String message) {
		super(message);
	}
	
	public BuildDataComputerException(Throwable cause) {
		super(cause);
	}
	
}
